package com.paymybuddy.paymybuddyapp.model;

public class BankAccount {
	private String IBAN;
	private int Amount;
	
	public String getIBAN() {
		return IBAN;
	}
	public void setIBAN(String iBAN) {
		IBAN = iBAN;
	}
	public int getAmount() {
		return Amount;
	}
	public void setAmount(int amount) {
		Amount = amount;
	}
	
	
}
